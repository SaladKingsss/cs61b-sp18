public class ArrayDeque<type>{
    public class IntNode {
        public LinkedListDeque.IntNode prev;
        public type item;
        public LinkedListDeque.IntNode next;

        public IntNode(LinkedListDeque.IntNode m, type i, LinkedListDeque.IntNode n) {
            prev = m;
            item = i;
            next = n;
        }
    }









}